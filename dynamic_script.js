function createCard() {
    var Recommended = document.getElementById('Recommended');
    var Superheroes = document.getElementById('Superheroes');
    var Specials = document.getElementById('Specials');
    var WatchwithFriends = document.getElementById('WatchwithFriends');

    var cardContainer = document.createElement('div');
    cardContainer.setAttribute('class', 'card-container');

    list.map(item => {
        var card = document.createElement('div');
        card.setAttribute('class', 'card');
        card.setAttribute('id', item.id);

        var img = document.createElement('img');
        img.src = item.image;
        img.setAttribute('class', 'card-img');
        img.setAttribute('alt', item.name);

        var cardBody = document.createElement('div');
        cardBody.setAttribute('class', 'card-body');

        var contentBody = document.createElement('div');
        contentBody.setAttribute('class', 'content-body');

        var name = document.createElement('h2');
        name.setAttribute('class', 'name');
        name.innerText = item.name;

        var des = document.createElement('h6');
        des.setAttribute('class', 'des');
        des.innerText = item.des;

        contentBody.appendChild(name);
        contentBody.appendChild(des);

        var watchlist = document.createElement('button');
        watchlist.setAttribute('class', 'watchlist-btn');
        watchlist.innerText = 'add to watchlist';

        cardBody.appendChild(contentBody);
        cardBody.appendChild(watchlist);

        card.appendChild(img);
        card.appendChild(cardBody);
        
        if(item.recommended){
            Recommended.appendChild(card)
        }
        if (item.superheroes) {
            Superheroes.appendChild(card)
        }
        if (item.specials) {
            Specials.appendChild(card)
        }
        if (item.friends) {
            WatchwithFriends.appendChild(card)
        }
    });
}

createCard();